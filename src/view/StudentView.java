    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if (txtaddress.getText().equalsIgnoreCase("") || txtemail.getText().equalsIgnoreCase("") || txtid.getText().equalsIgnoreCase("")
                || txtname.getText().equalsIgnoreCase("") || txtmark.getText().equalsIgnoreCase("")
                || txtphonenumber.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(rootPane, "Please fill out the form!");
        } else {
            if (jTable1.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(rootPane, "Please selected row!");
            } else {
                Student s = new Student();
                s.setAddress(txtaddress.getText());
                s.setEmail(txtemail.getText());
                s.setID(txtid.getText());
                s.setName(txtname.getText());
                s.setPhone(txtphonenumber.getText());
                try {
                    s.setMark(Float.parseFloat(txtmark.getText()));
                    if (act.modifiedStudent(s, model.getValueAt(jTable1.getSelectedRow(), 1).toString())) {
                        JOptionPane.showMessageDialog(rootPane, "Modified Success!");
                        model.setValueAt(evt, jTable1.getSelectedRow(), NORMAL);
                        txtaddress.setText("");
                        txtemail.setText("");
                        txtid.setText("");
                        txtmark.setText("");
                        txtname.setText("");
                        txtphonenumber.setText("");
                        model.setValueAt(jTable1.getSelectedRow() + 1, jTable1.getSelectedRow(), 0);
                        model.setValueAt(s.getID(), jTable1.getSelectedRow(), 1);
                        model.setValueAt(s.getName(), jTable1.getSelectedRow(), 2);
                        model.setValueAt(s.getAddress(), jTable1.getSelectedRow(), 3);
                        model.setValueAt(s.getPhone(), jTable1.getSelectedRow(), 4);
                        model.setValueAt(s.getEmail(), jTable1.getSelectedRow(), 5);
                        model.setValueAt(s.getMark(), jTable1.getSelectedRow(), 6);
                       
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Error!");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Mark is float");
                }
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

