    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        ComboBoxModel boxModel = jComboBox1.getModel();
        if (txtSearch.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(rootPane, "Not search keyword entered!");
        } else {
            if (act.getSearchList(boxModel.getSelectedItem().toString(), txtSearch.getText()).isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "No result is found!");
            } else {
                System.out.println(act.getSearchList(boxModel.getSelectedItem().toString(), txtSearch.getText()));
                list.clear();
                model.setRowCount(0);
                list.addAll(act.getSearchList(boxModel.getSelectedItem().toString(), txtSearch.getText()));
                showTable();
            }
        }
        txtSearch.setText("");
    }//GEN-LAST:event_jButton6ActionPerformed

